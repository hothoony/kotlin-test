package basic.basic05_types

fun main() {
    
    var boolTrue: Boolean = true
    var boolFalse: Boolean = false
    var boolNull: Boolean? = null   // Boolean 뒤에 ? 를 사용하면 null 입력 가능
    
    println("boolTrue  = $boolTrue")
    println("boolFalse = $boolFalse")
    println("boolNull  = $boolNull")
}