package basic.basic05_types

fun main() {
    
    nubmer_floatingPointType_Float()
    nubmer_floatingPointType_Double()
}

fun nubmer_floatingPointType_Float() {
    
    // Float
    // 뒤에 f 를 붙인다
    var numFloat1: Float =  3.14f
    var numFloat2 =  3.14f
    
    println("numFloat1 = $numFloat1")
    println("numFloat2 = $numFloat2")
}

fun nubmer_floatingPointType_Double() {

    // Double
    var numDouble1: Double = 1.0
    var numDouble2 = 1.0

    println("numDouble1 = $numDouble1")
    println("numDouble2 = $numDouble2")
}
